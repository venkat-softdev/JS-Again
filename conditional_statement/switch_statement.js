// switch statement in javascript 

let num=2;

switch (num) {
    case 1:
        console.log("one");
        
        break;
         
        case 2:
            console.log("two");
            break;
         
         case 3:
            console.log("Three");
            break;
               
        default:
            console.log("invalid number");
            
        break;
}