// math functions in javascript 

// pi value
let a=Math.PI;
console.log(a);

// round
let b=23.389;
b=Math.round(b);
console.log("round :",b);

// floor

let c=10.568;
c=Math.floor(c);
console.log("floor :",c);

// ceil
let d=4.67;
d=Math.ceil(d);
console.log("ceil :",d);

// sqrt

let e=9;
e=Math.sqrt(e);
console.log("sqrt :",e);

// power

let f;
f=Math.pow(2,4);
console.log("power :",f);

// max
let max;
max=Math.max(10,20,30,40,50,80);
console.log("max value :",max);

// min 
let min=(10,20,30,40,50,4);
min=Math.min(min);
console.log("Min value :",min);

 
